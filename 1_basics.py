# Print statements
#-------------------------------------------------------------------------------------
# A print statement is a kind of `method`.
# We will talk about methods extensively later, but for now it is important to 
# know that you can `call` methods, usually by putting some parentheses() 
# after them with some information we want the method to use. 
#
# Each piece of this information is called a `parameter`.
# print is a very simple method, so we will only be using it to take one or zero 
# parameters, as in:
# print()
# print(parameter)
# There are some more examples below!

print(1) #Bonus! You can also use comments after lines of code!
print("What is within these quotes is what programmers like to refer to as a 'string'.")
print("print can print strings(of letters), integers(numbers), and most ")
print("anything else you would like, though it might look a little weird!")
print()
print(2)
print("Now, if you are telling the program where the string ends with a quote,")
print("what if you want to put a quote in the string?")
print("That is where 'escape sequences' come in!")
print("python's string literal escape character is the backslash. ")
print("What that means is that if you put a backslash in the string, ")
print("it will know to take the next character absolutely literally,")
print("like so: \"And that is how you escape society's unfair rules!\"")
print("The quote is not the only character you might want to escape.")
print("For example, what if you wanted to use a backslash? ")
print("Easy; Just escape it! \\:D/")
print("Escape sequences are not just for letting you use characters that python would")
print("otherwise interpret differently, they are also for using characters that would")
print("otherwise be hard to type.")
print("for example, \\n \n makes a new line!")
print("There are a lot of fancy things you can do with just strings and print.")

